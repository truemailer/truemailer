    usage = get_daily_usage(client_name)
    if usage >= limit:
        raise HTTPException(status_code=429, detail="Daily limit reached")

    # do verify
    res = verify_email_address(payload.email)
    # increment usage
    increment_usage(client_name)
    return {"email": payload.email, **res}

